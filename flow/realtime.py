def _parse_summary_vec(s: str, n: int, name: str) -> List[float]:
    """Parse a CLI summary-control argument as either a single scalar (broadcast
    to length n) or a comma-separated list of exactly n floats.
    """
    parts = [p.strip() for p in str(s).split(",") if p.strip() != ""]
    if len(parts) == 1:
        return [float(parts[0])] * n
    if len(parts) != n:
        raise ValueError(
            f"--{name}: expected a scalar or {n} comma-separated floats, got {len(parts)}"
        )
    return [float(p) for p in parts]


