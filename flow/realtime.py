"""Real-time streaming audio generation: FlowDiT + CoDiCodec `decode_next()`.

Generates audio chunk-by-chunk at runtime: one CoDiCodec chunk
(~32_768 samples = ~683 ms at 48 kHz) per generation step. Each chunk is
streamed through `EncoderDecoder.decode_next()` straight to the system audio
output via `sounddevice`.

Per-chunk pipeline
------------------
    1.  Sliding-window prefix latent context (in raw codec space).
    2.  `cfm_sample()` -> `block_size` new latent tokens (one chunk).
    3.  `CodecWrapper.decode_next_chunk()` -> ~32_768 stereo samples.
    4.  Push to a thread-safe `AudioBuffer`.
    5.  `sounddevice.OutputStream` callback pulls fixed-size frames -> speakers.

Architectural differences vs. the example CAM realtime script
-------------------------------------------------------------
*   FlowDiT is block-causal: every chunk is generated by re-running the full
    transformer over the prefix + the new chunk's `block_size` tokens. There
    is no KV cache; instead we keep a *latent buffer* of past tokens and slide
    a fixed-length window across it (`--context-chunks`).
*   The denoising loop is rectified-flow CFM (`flow.model.cfm.sample`), not
    score-based diffusion. Solvers are `euler` and `heun`. The "temperature"
    knob simply rescales the predicted velocity each ODE step (smaller
    temperature -> stronger drift toward the data manifold; larger ->
    more variance).
*   Initial unconditional context is just an empty prefix `[1, 0, latent_dim]`,
    optionally seeded with a small amount of frozen Gaussian noise the model
    can latch onto via `--seed-scale`.
*   `--seed`, `s`/`S`/`x`/`X` keyboard actions reseed Python/Torch/NumPy RNGs
    and reset the codec's streaming state via `CodecWrapper.reset_streaming()`.

Example
-------
    python -m flow.realtime \
        --ckpt ./runs/v3_okachihuali/last.pt \
        --use-ema \
        --device mps \
        --solver euler \
        --nfe 4 \
        --context-chunks 32 \
        --temperature 1.0
"""

