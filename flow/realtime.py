def load_model(ckpt_path: str, device: torch.device, *, use_ema: bool) -> FlowDiT:
    """Load a FlowDiT checkpoint. Optionally swap in EMA weights (recommended)."""
    sd = torch.load(ckpt_path, map_location="cpu", weights_only=False)
    if "config" not in sd or "model" not in sd["config"]:
        raise RuntimeError(
            f"checkpoint {ckpt_path} has no 'config.model' field; cannot rebuild architecture"
        )
    model_cfg = ModelConfig(**sd["config"]["model"])
    model = FlowDiT(
        latent_dim=model_cfg.latent_dim,
        block_size=model_cfg.block_size,
        dim=model_cfg.dim,
        n_layers=model_cfg.n_layers,
        n_heads=model_cfg.n_heads,
        head_dim=model_cfg.head_dim,
        mlp_mult=model_cfg.mlp_mult,
        cond_dim=model_cfg.cond_dim,
        max_seq_len=model_cfg.max_seq_len,
        dropout=0.0,  # no dropout at inference
    )
    if use_ema:
        ema_state = sd.get("ema")
        if ema_state is None:
            raise RuntimeError(f"--use-ema set but {ckpt_path} has no 'ema' field")
        ema = EMA(model, decay=0.0)
        ema.load_state_dict(ema_state)
        ema.copy_to(model)
        logger.info("Loaded EMA weights from %s", ckpt_path)
    else:
        if "model" not in sd:
            raise RuntimeError(f"checkpoint {ckpt_path} has no 'model' field")
        model.load_state_dict(sd["model"])
        logger.info("Loaded raw (non-EMA) weights from %s", ckpt_path)
    model.to(device).eval()
    return model


# --------------------------------------------------------------------------- #
# Single-chunk CFM sampler (in normalized space, with temperature).
# --------------------------------------------------------------------------- #

@torch.no_grad()
def diffuse_chunk(
    model: FlowDiT,
    prefix_norm: torch.Tensor,
    *,
    n_steps: int,
    solver: str,
    temperature: float,
    init_noise: Optional[torch.Tensor] = None,
) -> torch.Tensor:
    """Generate one chunk (`block_size` tokens) given a clean *normalized* prefix.

    Args:
        model: trained FlowDiT (eval mode).
        prefix_norm: `[1, P, latent_dim]` already in normalized space.
        n_steps: number of ODE integration steps.
        solver: 'euler' | 'heun'.
        temperature: scales the predicted velocity each step
            (`x <- x - (dt / temperature) * v`). 1.0 = standard rectified-flow
            ODE; <1 sharpens; >1 diffuses.
        init_noise: optional `[1, block_size, latent_dim]` to start from
            (otherwise sampled from `N(0, I)` per call).

    Returns:
        `[1, block_size, latent_dim]` newly generated chunk in **normalized**
        space (caller denormalizes before feeding to the codec).
    """
    device = next(model.parameters()).device
    dtype = next(model.parameters()).dtype
    B = prefix_norm.shape[0]
    P = prefix_norm.shape[1]
    bs = model.block_size
    L = P + bs
    if L > model.max_seq_len:
        raise ValueError(f"context (P={P}) + block ({bs}) exceeds max_seq_len={model.max_seq_len}")

    if init_noise is None:
        target = torch.randn(B, bs, model.latent_dim, device=device, dtype=dtype)
    else:
        target = init_noise.to(device=device, dtype=dtype)

    x = torch.cat([prefix_norm, target], dim=1)
    attn_mask = block_causal_mask(L, model.block_size, device=device).to(dtype)

    def t_per_token(t_now: float) -> torch.Tensor:
        # Prefix kept clean (t=0); only the new chunk carries noise level t_now.
        is_target = torch.arange(L, device=device) >= P
        return is_target.float().unsqueeze(0).expand(B, -1) * t_now

    inv_temp = 1.0 / max(1e-6, float(temperature))
    dt = 1.0 / n_steps
    for k in range(n_steps):
        t_now = 1.0 - k * dt
        t_next = max(0.0, t_now - dt)

        if solver == "euler":
            v = model(x, t_per_token(t_now), attn_mask=attn_mask)
            x = x.clone()
            x[:, P:] = x[:, P:] - dt * inv_temp * v[:, P:]
        elif solver == "heun":
            v1 = model(x, t_per_token(t_now), attn_mask=attn_mask)
            x_pred = x.clone()
            x_pred[:, P:] = x_pred[:, P:] - dt * inv_temp * v1[:, P:]
            v2 = model(x_pred, t_per_token(t_next), attn_mask=attn_mask)
            x = x.clone()
            x[:, P:] = x[:, P:] - 0.5 * dt * inv_temp * (v1[:, P:] + v2[:, P:])
        else:
            raise ValueError(f"unknown solver: {solver!r} (expected 'euler' or 'heun')")

    return x[:, P:]


# --------------------------------------------------------------------------- #
# Audio buffer (thread-safe, O(1) status tracking).
# --------------------------------------------------------------------------- #

class AudioBuffer:
    """Bridges variable-size decoded chunks to fixed-size sounddevice callbacks.

    Also maintains a fixed-size ring of *recently-played* mono samples so the
    GUI can visualise what the user is actually hearing (not what is queued).
    """

    # ~2 s at 48 kHz of mono playback history is enough for any visualiser.
    _RECENT_RING_SAMPLES = 96_000

    def __init__(self, channels: int = 2):
        self._lock = threading.Lock()
        self._chunks: collections.deque = collections.deque()
        self._offset = 0
        self._channels = int(channels)
        self._buffered = 0
        self.underruns = 0
        self.total_samples = 0
        # Recent-playback ring (mono mix). Written every `pull()`.
        self._recent = np.zeros(self._RECENT_RING_SAMPLES, dtype=np.float32)
        self._recent_idx = 0  # next write position (wraps)

    def push(self, audio: np.ndarray) -> None:
        if audio.ndim == 1:
            audio = np.stack([audio, audio], axis=-1)
        with self._lock:
            self._chunks.append(audio.astype(np.float32, copy=False))
            self.total_samples += len(audio)
            self._buffered += len(audio)

    def pull(self, frames: int) -> np.ndarray:
        out = np.zeros((frames, self._channels), dtype=np.float32)
        filled = 0
        with self._lock:
            while filled < frames and self._chunks:
                chunk = self._chunks[0]
                avail = len(chunk) - self._offset
                take = min(avail, frames - filled)
                out[filled: filled + take] = chunk[self._offset: self._offset + take]
                self._offset += take
                filled += take
                if self._offset >= len(chunk):
                    self._chunks.popleft()
                    self._offset = 0
            self._buffered = max(0, self._buffered - filled)
            if filled < frames:
                self.underruns += 1
            # Mirror the audio that just went out into the recent ring (mono mix).
            if filled > 0:
                mono = out[:filled].mean(axis=1) if out.shape[1] > 1 else out[:filled, 0]
                ring = self._recent
                ring_n = ring.shape[0]
                idx = self._recent_idx
                end = idx + filled
                if end <= ring_n:
                    ring[idx:end] = mono
                else:
                    first = ring_n - idx
                    ring[idx:] = mono[:first]
                    ring[: end - ring_n] = mono[first:]
                self._recent_idx = end % ring_n
        return out

    @property
    def buffered_samples(self) -> int:
        return max(0, self._buffered)

    def recent_waveform(self, n: int) -> np.ndarray:
        """Return the last `n` mono samples that were actually played.

        Cheap O(n) read; safe to call from another thread.
        """
        with self._lock:
            ring = self._recent
            ring_n = ring.shape[0]
            n = max(0, min(int(n), ring_n))
            if n == 0:
                return np.zeros(0, dtype=np.float32)
            idx = self._recent_idx
            start = (idx - n) % ring_n
            if start + n <= ring_n:
                return ring[start: start + n].copy()
            first = ring_n - start
            out = np.empty(n, dtype=np.float32)
            out[:first] = ring[start:]
            out[first:] = ring[: n - first]
            return out

    def clear(self) -> None:
        with self._lock:
            self._chunks.clear()
            self._offset = 0
            self._buffered = 0


