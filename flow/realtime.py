def _build_argparser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(
        description="Real-time streaming generation: FlowDiT + CoDiCodec.decode_next().",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    p.add_argument("--ckpt", required=True, help="Path to last.pt or ema.pt produced by training.")
    p.add_argument("--use-ema", action="store_true",
                   help="Load EMA weights from the checkpoint (recommended for inference).")

    # Diffusion / sampler
    p.add_argument("--nfe", type=int, default=4, dest="n_steps",
                   help="Number of ODE steps per chunk.")
    p.add_argument("--solver", default="euler", choices=["euler", "heun"],
                   help="ODE solver. heun is 2x more accurate but 2x slower.")
    p.add_argument("--temperature", type=float, default=1.0,
                   help="Velocity scaling. <1 sharpens, >1 diffuses.")
    p.add_argument("--seed-scale", type=float, default=0.0,
                   help="Shrink initial noise toward 0 (0 = standard N(0, I)).")

    # Streaming / context
    p.add_argument("--context-chunks", type=int, default=32,
                   help="Sliding-window context length, in codec chunks "
                        "(1 chunk ≈ 0.683 s @ 48 kHz). Pass <=0 to use the "
                        "model's maximum safe context (max_seq_len // "
                        "block_size - 1).")
    p.add_argument("--prebuffer", type=int, default=2,
                   help="Number of chunks to render before starting playback.")
    p.add_argument("--crossfade-chunks", type=int, default=4,
                   help="Default crossfade length when switching seeds.")

    # Run control
    p.add_argument("--max-chunks", type=int, default=None,
                   help="Stop after this many chunks (default: run until 'q').")
    p.add_argument("--save", type=str, default=None,
                   help="Optional path to save the full session as a .wav file.")
    p.add_argument("--seed", type=int, default=None,
                   help="Initial RNG seed (default: time-based).")
    p.add_argument("--device", default=None,
                   help="cuda | mps | cpu (default: auto-detect).")

    # Summary-latent control (per-chunk, block_size tokens). Modulates each
    # chunk in normalized space:  x[:, i, :] <- x[:, i, :] * scale[i] + bias[i].
    # Pass either a single float (broadcast to all 8 tokens) or 8 comma-
    # separated floats. These can be adjusted live via b/B, g/G, i, o keys.
    p.add_argument("--summary-scale", type=str, default="1.0",
                   help="Initial per-token scale for the chunk's summary latents. "
                        "Scalar broadcasts to all tokens; or pass 8 comma-separated "
                        "floats. Default 1.0 (identity).")
    p.add_argument("--summary-bias", type=str, default="0.0",
                   help="Initial per-token bias (added in normalized space) for "
                        "the chunk's summary latents. Same format as --summary-scale. "
                        "Default 0.0.")
    return p


def main() -> None:
    args = _build_argparser().parse_args()
    device = best_device(args.device)
    logger.info("device=%s", device)

    ckpt_path = str(Path(args.ckpt).expanduser().resolve())
    if not os.path.exists(ckpt_path):
        raise FileNotFoundError(f"checkpoint not found: {ckpt_path}")

    model = load_model(ckpt_path, device, use_ema=bool(args.use_ema))
    n_params = sum(p.numel() for p in model.parameters())
    logger.info("Model: %d params (%.2fM)", n_params, n_params / 1e6)

    codec = CodecWrapper(CodecConfig(device=str(device)))
    chunk_secs = codec.samples_per_chunk / codec.sample_rate
    logger.info(
        "Codec: chunk=%.3fs, block_size=%d tokens, latent_dim=%d",
        chunk_secs, model.block_size, model.latent_dim,
    )

    generator = RealtimeFlowGenerator(
        model=model,
        codec=codec,
        device=device,
        n_steps=args.n_steps,
        solver=args.solver,
        temperature=args.temperature,
        seed_scale=args.seed_scale,
        context_chunks=args.context_chunks,
        prebuffer_chunks=args.prebuffer,
        crossfade_chunks=args.crossfade_chunks,
        initial_seed=args.seed,
    )

    # Apply optional initial summary-latent controls from CLI.
    bs = generator.block_size
    init_scale = _parse_summary_vec(args.summary_scale, bs, "summary-scale")
    init_bias = _parse_summary_vec(args.summary_bias, bs, "summary-bias")
    generator.set_summary_vectors(scale=init_scale, bias=init_bias)
    if generator._summary_active():
        logger.info(
            "summary control: scale=%s bias=%s",
            generator._fmt_summary_vec(generator.summary_scale),
            generator._fmt_summary_vec(generator.summary_bias),
        )

    generator.run(max_chunks=args.max_chunks, save_path=args.save)


if __name__ == "__main__":
    main()
